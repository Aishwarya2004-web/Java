public class Solution {

    public static void main(String[] args) {
        int n=1;
        while(n<=1){
            System.out.println(n+" Hello World");
            int i=2;
            while(i<=2){
                System.out.println(i+" I am a file");
                int j=3;
                while(j<=3){
                    System.out.println(j+" Read me until end-of-file.");
                    j++;
                    i++;
                    n++;
                }
            }
        }
    }
}
    